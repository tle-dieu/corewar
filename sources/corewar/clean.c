#include "corewar.h"

void			freedom(t_env *e)
{
	t_proc	*ptr;
	t_proc	*tmp;

	ptr = e->proc;
	while (ptr)
	{
		tmp = ptr;
		ptr = ptr->next;
		free(tmp);
	}
}
